package co.edu.uniquindio.poo;

import java.util.Collection;
import java.util.LinkedList;

public class Reunion {
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////// variables ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public String descripcion;
    public String fecha;
    public String hora;
    public Collection<Contacto> asistentes;

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////// Constructor //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public Reunion(String descripcion, String fecha, String hora){
        this.descripcion=descripcion;
        this.fecha=fecha;
        this.hora=hora;
        asistentes = new LinkedList<>();

    }

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////// Metodos //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    /*
     * Metodo que permite verificar el asistente en la reunion
     * @param nombre, telefono
     * @return bandera
     */
    public boolean verificarAsistente(String nombre, String telefono){
        boolean bandera = false;
        for (Contacto contacto : asistentes) {
            if(contacto.getNombre().equals(nombre) && contacto.getTelefono().equals(telefono)){
                bandera = true;
            }
        }
        return bandera;
    }

    /*
     * Metodo que permite agregar un asistente a la reunion
     * @param contacto
     */
    public void agregarAsistente(Contacto contacto){
        if(!verificarAsistente(contacto.getNombre(), contacto.getTelefono())){
            asistentes.add(contacto);
        }
        else{
            System.out.println("el contacto " + contacto.getNombre() + " ya existe por lo tanto no se agrega.");
        }
    }
    public void eliminarAsistente(Contacto contacto){
        asistentes.remove(contacto);
    }


    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////// Getters y setters /////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public Collection<Contacto> getAsistentes() {
        return asistentes;
    }

    public void setAsistentes(Collection<Contacto> asistentes) {
        this.asistentes = asistentes;
    }

    @Override
    public String toString() {
        return "Reunion [descripcion=" + descripcion + ", fecha=" + fecha + ", hora=" + hora + ", asistentes="
                + asistentes + "]";
    }

    
    
}
